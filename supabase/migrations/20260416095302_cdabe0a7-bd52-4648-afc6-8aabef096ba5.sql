
CREATE OR REPLACE FUNCTION public.check_level_up()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $$
DECLARE
  xp_table int[] := ARRAY[
    100,135,182,246,332,425,544,697,892,1141,
    1461,1870,2393,3064,3921,5019,6425,8224,10526,13474,
    16438,20054,24466,29849,36415,44427,54201,66125,80672,98420,
    120072,146488,178716,218033,266000,324520,395915,483016,589280,718921,
    848327,1001026,1181210,1393828,1644717,1940766,2290104,2702323,3188741,3762715,
    4440004,5239204,6182261,7295068,8608180,10157652,11986030,14143515,16689348,19693431,
    22450511,25593582,29176684,33261420,37918019,43226541,49278257,56177213,64042023,73007906,
    83229013,94881075,108164425,123307444,140570487,160250355,182685404,208261361,237417952,270656465,
    297722111,327494322,360243755,396268130,435894943,479484438,527432881,580176169,638193786,702013165,
    772214482,849435930,934379523,1027817475,1130599222,1243659145,1368025059,1504827565,1655310321,1820841354
  ];
  tier text;
BEGIN
  -- Cap at level 100
  WHILE NEW.level < 100 AND NEW.xp >= NEW.xp_next LOOP
    NEW.xp := NEW.xp - NEW.xp_next;
    NEW.level := NEW.level + 1;
    IF NEW.level < 100 THEN
      NEW.xp_next := xp_table[NEW.level];
    ELSE
      NEW.xp_next := 0;
      NEW.xp := 0;
    END IF;
  END LOOP;

  -- Update combat_rank based on tier
  IF NEW.level >= 81 THEN tier := 'Legend';
  ELSIF NEW.level >= 61 THEN tier := 'Champion';
  ELSIF NEW.level >= 41 THEN tier := 'Contributor';
  ELSIF NEW.level >= 21 THEN tier := 'Achiever';
  ELSE tier := 'Explorer';
  END IF;
  NEW.combat_rank := tier;

  RETURN NEW;
END;
$$;

-- Recreate trigger (drop old one first)
DROP TRIGGER IF EXISTS trigger_check_level_up ON public.profiles;
CREATE TRIGGER trigger_check_level_up
BEFORE UPDATE OF xp ON public.profiles
FOR EACH ROW
EXECUTE FUNCTION public.check_level_up();
